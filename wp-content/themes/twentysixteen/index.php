function typeWord(dom,dir,textArr,position,speed,index){
	if(dir){
		if(position < textArr[index].length){
			//audio.play();
			dom.html(dom.html()+textArr[index][position]);
			position++;
			
		}else{ 
			dir = 0;
			
		}
		
		setTimeout(function(){
			typeWord(dom,dir,textArr,position,speed,index);
		},speed);
		
	}else{
		
		position--;
			
		setTimeout(function(){
				delWord(dom,dir,textArr,position,speed,index);
		},speed*10);
	}
}

function delWord(dom,dir,textArr,position,speed,index){
	var text = dom.html();
	var length = text.length;
	
	if(length > 0){ 
		text = text.substr(0,length-1);
		dom.html(text);
		setTimeout(function(){
			delWord(dom,dir,textArr,position,speed,index);
		},speed/5);
		
	}else{
		index = ++index % 2;
		setTimeout(function(){
			typeWord(dom,1,textArr,0,speed,index);
		},speed);
	}
}

